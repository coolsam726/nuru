def resolve_icon(icon_name: str) -> str:
    """
    Resolve an icon name to an SVG path.

    If the icon_name is already a full SVG path (contains spaces or common SVG
    keywords), return it as-is. Otherwise, look it up in the HEROICONS registry.

    Args:
        icon_name: Icon name (e.g., "home", "chart-bar") or a full SVG path

    Returns:
        SVG path d="..." string

    Example:
        >>> resolve_icon("home")
        'M3 12a9 9...'
        >>> resolve_icon("M3 12a9 9...")
        'M3 12a9 9...'  # returned as-is
    """
    if not icon_name:
        return ""

    # If it looks like a full SVG path (contains "M" at start or spaces), return as-is
    if icon_name.startswith("M") or " " in icon_name:
        return icon_name

    # Look up in heroicons registry
    return HEROICONS.get(icon_name.lower(), icon_name)
